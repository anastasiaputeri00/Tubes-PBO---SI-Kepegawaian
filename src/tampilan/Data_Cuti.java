    private void tblCutiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCutiMouseClicked
        // TODO add your handling code here:
        int baris = tblCuti.rowAtPoint(evt.getPoint());
        String nip = tblCuti.getValueAt(baris, 1).toString();
        txtNip.setText(nip);
        
        String nama = tblCuti.getValueAt(baris, 2).toString();
        txtNama.setText(nama);
        
        String pangkat = tblCuti.getValueAt(baris, 3).toString();
        cbPangkat.setSelectedItem(pangkat);
        
        String jabatan = tblCuti.getValueAt(baris, 4).toString();
        txtJabatan.setText(jabatan);
        
        String tawal = tblCuti.getValueAt(baris, 5).toString();
        txtTawal.setText(tawal);
        
        String takhir = tblCuti.getValueAt(baris, 6).toString();
        txtTakhir.setText(takhir);
        
        String keterangan = tblCuti.getValueAt(baris, 7).toString();
        txtKeterangan.setText(keterangan);
    }//GEN-LAST:event_tblCutiMouseClicked

